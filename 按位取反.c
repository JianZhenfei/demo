#include <stdio.h>
#include <string.h>

int main()
{
    int a = 10;//4个字节，32bit位
    int b = ~a;//b是有符号的整形
    int c = a++;//后置++,先使用再++
    int d = ++a;//前置++，先++再使用
    //~按（2进制）位取反
    //00000000000000000000000000000000
    //11111111111111111111111111111111
    //原码，反码，补码
    //负数在内存中存储的时候，存储的是二进制的补码
    printf("%d\n%d\n%d\n",b,c,d);
    //11111111111111111111111111111111
    //11111111111111111111111111111110
    //10000000000000000000000000000001


    return 0;
}